package com.example.demo.domain;

public class DateCalcData {

	private int dateId;
	private String dateName;
	private int plusLessYear;
	private int plusLessMonth;
	private int plusLessDay;

	public int getDateId() {
		return dateId;
	}
	public void setDateId(int dateId) {
		this.dateId = dateId;
	}
	public String getDateName() {
		return dateName;
	}
	public void setDateName(String dateName) {
		this.dateName = dateName;
	}
	public int getPlusLessYear() {
		return plusLessYear;
	}
	public void setPlusLessYear(int plusLessYear) {
		this.plusLessYear = plusLessYear;
	}
	public int getPlusLessMonth() {
		return plusLessMonth;
	}
	public void setPlusLessMonth(int plusLessMonth) {
		this.plusLessMonth = plusLessMonth;
	}
	public int getPlusLessDay() {
		return plusLessDay;
	}
	public void setPlusLessDay(int plusLessDay) {
		this.plusLessDay = plusLessDay;
	}



}
